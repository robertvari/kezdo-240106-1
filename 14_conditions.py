number = 10

#     True
if number == 10:
    print("The number is 10")

#     False
if number != 10:
    print("The number is not 10")

#     False
if number < 10:
    print("The number is less then 10")

#     False
if number > 10:
    print("The number is greater than 10.")

#    True
if number <= 10:
    print("The number is less or equal to 10")

#   True
if number >= 10:
    print("The number is greater or equal to 10.")